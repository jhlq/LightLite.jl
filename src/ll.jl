function toffoli!(ps::Photons,cct::Array{Int})
	T=rz(pi/4)
	Tdg=rz(-pi/4)
	apply!(ps,cct[3],H)
	apply!(ps,[cct[2],cct[3]],cnot)
	apply!(ps,cct[3],Tdg)
	apply!(ps,[cct[1],cct[3]],cnot)
	apply!(ps,cct[3],T)
	apply!(ps,[cct[2],cct[3]],cnot)
	apply!(ps,cct[3],Tdg)
	apply!(ps,[cct[1],cct[3]],cnot)
	apply!(ps,cct[2],Tdg)
	apply!(ps,cct[3],T)
	apply!(ps,[cct[1],cct[2]],cnot)
	apply!(ps,cct[3],H)
	apply!(ps,cct[2],Tdg)
	apply!(ps,[cct[1],cct[2]],cnot)
	apply!(ps,cct[1],T)
	apply!(ps,cct[2],S)
end

